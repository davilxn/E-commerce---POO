    public HistoricoDeCompras(Cliente cliente) throws SQLException {
        initComponents();

        this.cliente = cliente;

        Connection conexao = new Conexao().getConnection();
        PedidosDAO pedidosDAO = new PedidosDAO(conexao);

        this.pedidoAuxs = pedidosDAO.selectProdutoParaHistorico(cliente.getId());
        readJtable();
    }
    
    public void readJtable(){
        DefaultTableModel model = (DefaultTableModel) tabelaProdutos.getModel();
        model.setRowCount(0);
        pedidoAuxs.forEach(ped -> {
            model.addRow(new Object[]{
                ped.getId_produto(),
                ped.getCnpj_vendedor(),
                ped.getQuant_selecionada(),
                ped.getPreco_produto(),
                ped.getId_cliente(),
                ped.getEmail_cliente(),
                ped.getStatus()
            });
        });
        
    }

